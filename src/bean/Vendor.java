package bean;

public class Vendor {
	private String vid;
	private String vname;
	private String vadd;

	public String getVid() {
		return vid;
	}

	public void setVid(String vid) {
		this.vid = vid;
	}

	public String getVname() {
		return vname;
	}

	public void setVname(String vname) {
		this.vname = vname;
	}

	public String getVadd() {
		return vadd;
	}

	public void setVadd(String vadd) {
		this.vadd = vadd;
	}
}
