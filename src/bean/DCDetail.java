package bean;

public class DCDetail {
	private String dcId;
	private String dcPhone;
	private String address1;
	private String address2;
	private String pin;
	private String city;
	private String state;
	private String landmart;

	public String getDcId() {
		return dcId;
	}

	public void setDcId(String dcId) {
		this.dcId = dcId;
	}

	public String getDcPhone() {
		return dcPhone;
	}

	public void setDcPhone(String dcPhone) {
		this.dcPhone = dcPhone;
	}

	public String getAddress1() {
		return address1;
	}

	public void setAddress1(String address1) {
		this.address1 = address1;
	}

	public String getAddress2() {
		return address2;
	}

	public void setAddress2(String address2) {
		this.address2 = address2;
	}

	public String getPin() {
		return pin;
	}

	public void setPin(String pin) {
		this.pin = pin;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
	}

	public String getLandmart() {
		return landmart;
	}

	public void setLandmart(String landmart) {
		this.landmart = landmart;
	}
}
